#!/bin/bash
# ==============================================================================
# Script: SLES SP6 → SP7 アップグレード前環境調査
# 生成日: 2026-03-17
# 対象VM: instance-20260317-154124 / asia-northeast1-b / tech-trend-1762180118
# 接続方式: gcloud-ssh
# ==============================================================================
# 注意: このスクリプトは読み取り専用コマンドのみ実行します。
#       破壊的操作・状態変更操作は含みません。
# ==============================================================================

set -euo pipefail

VM_NAME="instance-20260317-154124"
ZONE="asia-northeast1-b"
PROJECT_ID="tech-trend-1762180118"
LOG_DIR="$(cd "$(dirname "$0")/../../logs" && pwd)"
LOG_FILE="${LOG_DIR}/20260317_SLES_SP6_SP7_upgrade_precheck.log"

SSH="gcloud compute ssh ${VM_NAME} --zone=${ZONE} --project=${PROJECT_ID} --"

log() {
  echo "$1" | tee -a "${LOG_FILE}"
}

run_cmd() {
  local label="$1"
  local cmd="$2"
  log ""
  log "=== ${label} ==="
  ${SSH} "${cmd}" 2>&1 | tee -a "${LOG_FILE}"
}

# ------------------------------------------------------------------------------
# 初期化
# ------------------------------------------------------------------------------
mkdir -p "${LOG_DIR}"
{
  echo "=== CLI Validation Log ==="
  echo "Date: $(date)"
  echo "Goal: SLES SP6からSP7へのアップグレード前環境調査"
  echo "Target: ${VM_NAME} / ${ZONE} / ${PROJECT_ID}"
} > "${LOG_FILE}"

log "調査開始: $(date)"

# ------------------------------------------------------------------------------
# [1] OS情報
# ------------------------------------------------------------------------------
run_cmd "[1] OS情報" "cat /etc/os-release"

# ------------------------------------------------------------------------------
# [2] カーネル・アーキテクチャ
# ------------------------------------------------------------------------------
run_cmd "[2] カーネル・アーキテクチャ" "uname -r && uname -m"

# ------------------------------------------------------------------------------
# [3] ホスト名
# ------------------------------------------------------------------------------
run_cmd "[3] ホスト名" "hostname"

# ------------------------------------------------------------------------------
# [4] ディスク使用量
# ------------------------------------------------------------------------------
run_cmd "[4] ディスク使用量" "df -h"

# ------------------------------------------------------------------------------
# [5] ブロックデバイス一覧
# ------------------------------------------------------------------------------
run_cmd "[5] ブロックデバイス" "lsblk"

# ------------------------------------------------------------------------------
# [6] ネットワークインターフェース
# ------------------------------------------------------------------------------
run_cmd "[6] ネットワーク" "ip addr show"

# ------------------------------------------------------------------------------
# [7] 稼働中サービス一覧
# ------------------------------------------------------------------------------
run_cmd "[7] 稼働中サービス" "systemctl list-units --type=service --state=running --no-pager"

# ------------------------------------------------------------------------------
# [8] SUSE登録状態
# ------------------------------------------------------------------------------
run_cmd "[8] SUSE登録状態" "sudo SUSEConnect --status"

# ------------------------------------------------------------------------------
# [9] SPマイグレーション照会
# ------------------------------------------------------------------------------
run_cmd "[9] SPマイグレーション照会" "sudo zypper migration --query"

# ------------------------------------------------------------------------------
# [10] リポジトリ設定
# ------------------------------------------------------------------------------
run_cmd "[10] リポジトリ設定" "zypper repos"

# ------------------------------------------------------------------------------
# [11] 利用可能なアップデート一覧
# ------------------------------------------------------------------------------
run_cmd "[11] 利用可能なアップデート" "sudo zypper lu"

# ------------------------------------------------------------------------------
# [12] インストール済み製品確認
# ------------------------------------------------------------------------------
run_cmd "[12] インストール済み製品" "sudo zypper --disable-repositories --non-interactive products -i"

# ------------------------------------------------------------------------------
# 完了
# ------------------------------------------------------------------------------
log ""
log "=== 調査完了: $(date) ==="
log "ログ出力先: ${LOG_FILE}"
